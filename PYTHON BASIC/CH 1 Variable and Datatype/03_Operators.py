# OPERATORS ARE ASSIGN TO CONVERT COMPLEX PROGRAMING TO SIMPLE WAY

# 1 - Arithmetic operators :- + , - , * , / , %
# EXAMPLE

a = 10
b = 25

print(
    a + b ,"\n",
    a - b ,"\n",
    a * b ,"\n",
    a / b ,"\n",
    a % b ,"\n",

)

# ASSIGNMENT OPERATORS :- = , += , -= , *= , /= , %=
# EXAMPLE

# a = b,      #B VALUE ASSIGN IN A
# a += b,     # A VALUE INCRESS BY B VALUE LIKE :-10 = 10+25
# a -= b,     #A VALUE DICRESS BY B VALUE LIKE :- 10 = 10-25
# a *= b,     #A VALUE MULTIPLY BY B VALUE LIKE :- 10 = 10*25
# a /= b ,    #A VALUE DEVIDE BY B VALUE LIKE :- 10 = 10/25
# a %= b,     #A VALUE MODULE BY B VALUE LIKE :- 10 = 10%25

