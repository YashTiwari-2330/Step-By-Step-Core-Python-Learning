#TYPE CASTING() :-
                # TYPE CASTING() WILL USE TO CONVERT A DATATYPE INTO A ANOTHER DATATYPE LIKE:-INT TO STRINGT , STRING TO INT
#EXAMPLE

#INTEGER TO STRING
a = str(31)
print(type(a) , a)

#STRING TO INTEGER
b = int("32")
print(b , type(b))

#INTEGER TO FLOAT
d = float(32)
print(d, type(d))

#FLOAT TO INTEGER
c = int(32.12)
print(c , type(c))


